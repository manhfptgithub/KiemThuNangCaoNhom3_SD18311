
package Entity;


public class DoanhThu {
    private float doanhThuTong;
    private int soluongHD;
    private float DoanhThuPGG;

    public DoanhThu() {
    }

    public float getDoanhThuTong() {
        return doanhThuTong;
    }

    public void setDoanhThuTong(float doanhThuTong) {
        this.doanhThuTong = doanhThuTong;
    }

    public int getSoluongHD() {
        return soluongHD;
    }

    public void setSoluongHD(int soluongHD) {
        this.soluongHD = soluongHD;
    }

    public float getDoanhThuPGG() {
        return DoanhThuPGG;
    }

    public void setDoanhThuPGG(float DoanhThuPGG) {
        this.DoanhThuPGG = DoanhThuPGG;
    }    
}
